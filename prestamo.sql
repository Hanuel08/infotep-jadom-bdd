/*AQUI ESTAN LOS ESTADOS DE LOS PRESTAMOS Y LAS CUOTAS
PR--Prestamo Registrado
PP--Prestamo Pagado

CR--Cuota Registrada
CP--Cuota Pagada
*/

/*BORRA LA BASE DE DATOS SI EXISTE*/
DROP DATABASE IF EXISTS PRESTAMO;

/*CREACION DE LA BASE DE DATOS*/
CREATE DATABASE PRESTAMO;

/*USO DE LA BASE DE DATOS*/
USE PRESTAMO;

/*CREACION DE LA TABLA CLIENTES*/
CREATE TABLE Clientes(
	id_cliente INT AUTO_INCREMENT,
	nombre VARCHAR(40) NOT NULL,
	apellido VARCHAR(40) NOT NULL ,
	cod_tipo_documento CHAR(1) NOT NULL,
	cod_documento CHAR(16) NOT NULL,
	telefono CHAR(10) NOT NULL,
	fecha_nacimiento DATE NOT NULL,
	genero CHAR(1) NOT NULL, 
	direccion VARCHAR(100) NOT NULL,
	estado CHAR(1) DEFAULT 'A', 
	CONSTRAINT CK_Clientes_cod_tipo_documento CHECK (cod_tipo_documento IN ('C','P')),
	CONSTRAINT CK_Clientes_documento CHECK (cod_tipo_documento != 'C' OR (cod_tipo_documento = 'C' AND cod_documento NOT LIKE '%[^0-9]%' AND LENGTH(cod_documento) = 11)),
	CONSTRAINT CK_Clientes_telefono CHECK (telefono RLIKE '[8][024][9][0-9]{7}'),
	CONSTRAINT CK_Clientes_genero CHECK (genero IN ('M','F')),
	CONSTRAINT CK_Clientes_estado CHECK (estado IN ('A','I')),
	CONSTRAINT UQ_Clientes_cod_documento UNIQUE (cod_documento),
	CONSTRAINT PK_Clientes PRIMARY KEY (id_cliente)
);

/*CREACION DE LA TABLA ROLES*/
CREATE TABLE Roles(
	id_rol INT AUTO_INCREMENT,
	nombre VARCHAR(100) NOT NULL,
	estado CHAR(1) DEFAULT 'A',
	CONSTRAINT CK_Roles_estado CHECK (estado IN ('A','I')),
	CONSTRAINT UQ_Roles_cod_nombre UNIQUE (nombre),
	CONSTRAINT PK_Roles PRIMARY KEY (id_rol)
);

/*CREACION DE LA TABLA SUCURSALES*/
CREATE TABLE Sucursales(
	id_sucursal INT AUTO_INCREMENT,
	nombre VARCHAR(100) NOT NULL,
	estado CHAR(1) DEFAULT 'A',
	CONSTRAINT CK_Sucursales_estado CHECK (estado IN ('A','I')),
	CONSTRAINT UQ_Sucursales_cod_nombre UNIQUE (nombre),
	CONSTRAINT PK_Sucursales PRIMARY KEY (id_sucursal)
);


/*CREACION DE LA TABLA PAISES*/
CREATE TABLE Paises(
	id_pais INT AUTO_INCREMENT,
	nombre VARCHAR(60) NOT NULL,
	name VARCHAR(60) NOT NULL,
	nom VARCHAR(60) NOT NULL,
	iso CHAR(2) NOT NULL,
	estado CHAR(1) DEFAULT 'A',
	CONSTRAINT CK_Paises_estado CHECK (estado IN ('A','I')),
	CONSTRAINT UQ_Paises_cod_nombre UNIQUE (nombre),
	CONSTRAINT PK_Paises PRIMARY KEY (id_pais)
); 


/*CREACION DE LA TABLA DEPARTAMENTOS*/
CREATE TABLE Departamentos(
	id_departamento INT AUTO_INCREMENT,
	nombre VARCHAR(60) NOT NULL,
	estado CHAR(1) DEFAULT 'A',
	CONSTRAINT CK_Departamentos_estado CHECK (estado IN ('A','I')),
	CONSTRAINT UQ_Departamentos_cod_nombre UNIQUE (nombre),
	CONSTRAINT PK_Departamentos PRIMARY KEY (id_departamento)
);

/*CREACION DE LA TABLA TIPO telefono*/
CREATE TABLE tipo_telefono(
	id_tipo_telefono INT AUTO_INCREMENT,
	tipo_telefono VARCHAR(10) NOT NULL,
	estado CHAR(1) DEFAULT 'A',
	CONSTRAINT CK_tipo_telefono_estado CHECK (estado IN ('A','I')),
	CONSTRAINT PK_tipo_telefono PRIMARY KEY (id_tipo_telefono)
);

/*CREACION DE LA TABLA EMPLEADOS*/
CREATE TABLE Empleados(
	id_empleado INT AUTO_INCREMENT,
	nombre VARCHAR(40) NOT NULL,
	apellido VARCHAR(40) NOT NULL,
	cod_tipo_documento CHAR(1) NOT NULL,
	cod_documento CHAR(16) NOT NULL,
	fecha_nacimiento DATE NOT NULL,
	email VARCHAR(60) NOT NULL,
	genero CHAR(1) NOT NULL,
	id_pais INT NOT NULL,
	condicion CHAR(1) DEFAULT 'A',
	estado_civil CHAR(1) NOT NULL,
	fecha_ingreso DATETIME NOT NULL DEFAULT NOW(),
	fecha_salida DATE,
	id_departamento INT NOT NULL,
	id_sucursal INT NOT null,
	id_empleado_supervisor INT,
	CONSTRAINT CK_Empleados_cod_tipo_documento CHECK (cod_tipo_documento IN ('C','P')),
	CONSTRAINT CK_Empleados_documento CHECK (cod_tipo_documento != 'C' OR (cod_tipo_documento = 'C' AND cod_documento NOT LIKE '%[^0-9]%' AND LENGTH(cod_documento) = 11)),
	CONSTRAINT CK_Empleados_genero CHECK (genero IN ('M','F')),
	CONSTRAINT CK_Empleados_condicion CHECK (condicion IN ('A','L','V','S')),
	CONSTRAINT CK_Empleados_estado_civil CHECK (estado_civil IN ('S','C','D','V','U')),
	CONSTRAINT UQ_Empleados_cod_documento UNIQUE (cod_documento),
	CONSTRAINT PK_Empleados PRIMARY KEY (id_empleado),
	CONSTRAINT FK_Empleados_Paises FOREIGN KEY (id_pais) REFERENCES Paises(id_pais),
	CONSTRAINT FK_Empleados_Departamentos FOREIGN KEY (id_departamento) REFERENCES Departamentos(id_departamento),
	CONSTRAINT FK_Empleados_Sucursales FOREIGN KEY (id_sucursal) REFERENCES Sucursales(id_sucursal),
	CONSTRAINT FK_Empleados_Empleados FOREIGN KEY (id_empleado_supervisor) REFERENCES Empleados(id_empleado)
);

/*CREACION DE LA TABLA telefono*/
CREATE TABLE telefono(
	id_telefono INT AUTO_INCREMENT,
	id_empleado INT NOT NULL,
	telefono CHAR(10) NOT NULL,
	id_tipo_telefono INT NOT NULL,
	estado CHAR(1) DEFAULT 'A',
	CONSTRAINT CK_telefono_telefono CHECK (telefono RLIKE '[8][024][9][0-9]{7}'),
	CONSTRAINT CK_telefono_estado CHECK (estado IN ('A','I')),
	CONSTRAINT PK_telefono PRIMARY KEY (id_telefono),
	CONSTRAINT FK_telefono_Empleados FOREIGN KEY (id_empleado) REFERENCES Empleados(id_empleado),
	CONSTRAINT FK_telefono_tipo_telefono FOREIGN KEY (id_tipo_telefono) REFERENCES tipo_telefono(id_tipo_telefono)
);

/*CREACION DE LA TABLA USUARIOS*/
CREATE TABLE Usuarios(
	id_usuario INT AUTO_INCREMENT,
	id_empleado INT NOT NULL,
	usuario VARCHAR(60) NOT NULL,
	pwd VARBINARY(128) NOT NULL,
	CONSTRAINT UQ_Usuarios_usuario UNIQUE (usuario),
	CONSTRAINT PK_Usuarios PRIMARY KEY (id_usuario),
	CONSTRAINT FK_Usuarios_Empleados FOREIGN KEY (id_empleado) REFERENCES Empleados(id_empleado)
);

/*CREACION DE LA TABLA PRESTAMOS*/
CREATE TABLE Prestamos(
	id_prestamo INT AUTO_INCREMENT,
	Cantidad_Prestada DECIMAL(18, 2) NOT NULL,
	Total_A_Ganar DECIMAL(18, 2) NOT NULL,
	Total_Pagar DECIMAL(18, 2) NOT NULL,
	Total_Ganado DECIMAL(18, 2) NOT NULL,
	Interes DECIMAL(4, 2) NOT NULL,
	Interes_Por_Cuota DECIMAL(18, 2) NOT NULL,
	Monto_Cuota DECIMAL(18, 2) NOT NULL,
	No_Cuotas_Prestamo INT NOT NULL,
	No_Cuotas_Pagadas INT NOT NULL,
	Frecuencia_Pago CHAR(1) NOT NULL,
	Id_Cliente INT NOT NULL,
	estado CHAR(2) NOT NULL,
	id_empleado_reg INT NOT NULL,
	Fecha_Reg DATE NOT NULL,
	id_empleado_mod INT,
	Fecha_Mod DATE,
	CONSTRAINT CK_Prestamos_Cantidad_Prestada CHECK (Cantidad_Prestada > 0),
	CONSTRAINT CK_Prestamos_Frecuencia_Pago CHECK (Frecuencia_Pago IN ('S','Q','M')),
	CONSTRAINT CK_Prestamos_estado CHECK (estado IN ('PR','PP')),
	CONSTRAINT PK_Prestamos PRIMARY KEY (id_prestamo),
	CONSTRAINT FK_Prestamos_Clientes FOREIGN KEY (id_cliente) REFERENCES Clientes(id_cliente),
	CONSTRAINT FK_Prestamos_Empleados_R FOREIGN KEY (id_empleado_reg) REFERENCES Empleados(id_empleado),
	CONSTRAINT FK_Prestamos_Empleados_M FOREIGN KEY (id_empleado_mod) REFERENCES Empleados(id_empleado)
);

/*CREACION DE LA TABLA DETALLE PAGOS*/
CREATE TABLE DetallePagos(
	id_detalle_pago INT AUTO_INCREMENT,
	id_prestamo INT NOT NULL,
	No_Cuota INT NOT NULL,
	Fecha_Pago DATE,
	Fecha_Vencimiento DATE NOT NULL,
	Dias_Atraso INT NOT NULL DEFAULT 0,
	Monto_Pagado DECIMAL(18, 2) NULL,
	Mora DECIMAL(18, 2) DEFAULT 0.00,
	estado CHAR(2) NOT NULL, 
	CONSTRAINT CK_DetallePagos_estado CHECK (Estado IN ('CR','CP','CA')),
	CONSTRAINT PK_DetallePagos PRIMARY KEY (id_detalle_pago),
	CONSTRAINT FK_DetallePagos_Clientes FOREIGN KEY (id_prestamo) REFERENCES Prestamos(id_prestamo)
);

